package ru.shze.test;

import ru.shze.sort.Sorter;

/**
 * Абстрактный тест для измерения времени работы сортировки.
 * Конкретные реализации задают тип входного массива и сценарий тестирования.
 *
 * @see TestParam
 * @see Sorter
 */
public abstract class Test {

    /**
     * Человекочитаемое имя теста (тип массива или сценарий).
     */
    final String name;

    /**
     * Создаёт новый тест с указанным именем.
     *
     * @param name название теста, отображаемое в выводе
     */
    public Test(String name) {
        this.name = name;
    }

    /**
     * Запускает тест для переданного сортировщика и параметров.
     * Должен подготовить массив нужного размера, выполнить серию прогонов
     * и вернуть лучшее (или среднее) время в миллисекундах.
     *
     * @param sorter реализация алгоритма сортировки
     * @param param  параметры теста (размер массива и число прогонов)
     * @return измеренное время выполнения в миллисекундах
     */
    public abstract int test(Sorter<Integer> sorter, TestParam param);
}
