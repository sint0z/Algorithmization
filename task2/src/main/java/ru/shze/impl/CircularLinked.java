package ru.shze.impl;

/**
 * Интерфейс циклического связного списка.
 * Особенность таких списков: последний элемент логически ссылается на первый,
 * позволяя «ходить по кругу» при обходе.
 *
 * @param <E> тип элементов списка
 */
public interface CircularLinked<E> extends SingleLinked<E>, Iterable<E> {

    /**
     * Выполняет циклический сдвиг элементов списка:
     * первый элемент становится последним, а остальная часть сдвигается вперёд на одну позицию.
     */
    void rotate();

    /**
     * Проверяет наличие цикла в структуре списка.
     * Для корректно реализованного циклического списка результат обычно всегда {@code true}.
     *
     * @return {@code true}, если структура списка содержит цикл
     */
    boolean findCycle();

    /**
     * Выполняет поиск элемента по целочисленному значению с учётом циклической природы списка.
     * Конкретная реализация определяет, как интерпретировать значение {@code data}.
     *
     * @param data искомое значение
     * @return найденный элемент или {@code null}, если элемент не найден
     */
    E find(int data);

    /**
     * Разделяет текущий список на два примерно равных циклических списка.
     *
     * @return массив из двух новых списков: левый и правый;
     *         если исходный список пуст, оба списка могут быть пустыми
     */
    CircularLinked<E>[] splitIntoTwo();
}
